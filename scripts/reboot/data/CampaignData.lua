local CampaignData = {}

local COLOR_CODES = {
    A = "aqua",
    C = "coral",
    M = "mint",
    Y = "amber",
    V = "violet",
    R = "rose",
}

local function parseTube(code)
    local tube = {}
    for index = 1, #code do
        local key = string.sub(code, index, index)
        table.insert(tube, COLOR_CODES[key] or key)
    end
    return tube
end

local function level(id, title, tubeCodes, moveBudget, mechanics, tutorial)
    local tubes = {}
    for _, code in ipairs(tubeCodes) do
        table.insert(tubes, parseTube(code))
    end
    return {
        id = id,
        title = title,
        board = { capacity = 4, tubes = tubes },
        mechanics = mechanics,
        goals = { primary = "sort_all_colors", bonus = { moveBudget = moveBudget } },
        tutorial = tutorial,
    }
end

